#include <unistd.h>

int	ft_putchar(int c)
{
	write(1, &c, 1);
	return (0);
}

int	main(void)
{
	int n;

	n = 0;
	while (n <= 9)
	{
		ft_putchar(n);
		n++;
	}
	ft_putchar('\n');
	return (0);
}
